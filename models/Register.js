import mongoose from "mongoose";

const Register = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const Regis = mongoose.model("Regis", Register);
export default Regis;
